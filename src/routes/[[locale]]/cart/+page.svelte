<script lang="ts">
import { applyAction, deserialize } from '$app/forms'
import { invalidateAll } from '$app/navigation'
import { CartAction } from '$lib/types'
import QuantityUpdate from '$lib/components/Cart/QuantityUpdate.svelte'

let {data, form} = $props()

const {cart} = $derived(data)

const handleCartAction = async (e: Event, action: CartAction) => {
  e.preventDefault()

  const target = e.target as HTMLFormElement
  const body = new FormData(target)
  let response: Response

  if (action === CartAction.REMOVE_FROM_CART && !confirm('Are you sure you want to remove this item from your cart?'))
    return

  response = await fetch(`/cart?/${action}`, {
    method: 'POST',
    body,
  })

  const result = deserialize(await response.text())
  result.type === 'success' && await invalidateAll()
  applyAction(result)
}
</script>

<h1>Cart - {cart?.totalQuantity ?? 0}</h1>

{#if form}
  {#each form.errors || [] as error}
    <p>{error.message}</p>
  {/each}
{/if}

{#if !cart || cart?.lines?.edges.length === 0}
  <p>Cart is empty</p>
{:else}
  {#each cart?.lines?.edges.map((e) => e.node) as line}
    {@const {
      id,
      quantity,
      merchandise } = line}
    {@const {
      selectedOptions,
      product } = merchandise}

    <div class="flex flex-col items-start gap-2 p-2 bg-neutral-100">
      <a href="/products/{product.handle}">{product.title}</a>
      <div class="text-sm">
        <p>{quantity || 0} x {line.cost.amountPerQuantity.amount} = {line.cost.totalAmount.amount}</p>
        {#if merchandise.title !== 'Default Title'}
          {#each selectedOptions as option}
            <p>{option.name}: {option.value}</p>
          {/each}
        {/if}
      </div>
      <form onsubmit={(e) => handleCartAction(e, CartAction.UPDATE_CART)}>
        <QuantityUpdate
          {line}
          {quantity}
          maxQuantity={merchandise.quantityAvailable}/>
      </form>
      <form onsubmit={(e) => handleCartAction(e, CartAction.REMOVE_FROM_CART)}>
        <input type="hidden" name="lineIds" value={JSON.stringify([id])} />
        <button type="submit">Remove Item</button>
      </form>
    </div>
  {/each}

  <div class="flex flex-col gap-4">
    <form onsubmit={(e) => handleCartAction(e, CartAction.UPDATE_DISCOUNT)}>
      {#if cart.discountCodes?.length}
        <p>Discount</p>
        {#each cart.discountCodes as discount}
          <div>
            <span>{discount.code}</span>
            <button type="submit">Remove Code</button>
          </div>
        {/each}
      {:else}
        <input type="text" name="discountCode" placeholder="Discount Code" />
        <button type="submit">Apply Discount</button>
      {/if}
    </form>
    <a href={cart.checkoutUrl}>Continue to Checkout - {cart.cost.subtotalAmount.amount}</a>
  </div>
{/if}
